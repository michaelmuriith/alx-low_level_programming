#include <stdio.h>

/**
  *add - adds two digits
  *@a : first digit
  *@b : second digit
  *Return: result
  */

int add(int a, int b)
{
	return (a + b);
}

/**
  *sub - subtracts b from a
  *@a : first digit
  *@b : second digit
  *Return: result
  */
int sub(int a, int b)
{
	return (a - b);
}

/**
  *mul - finds the product of a and b
  *@a : first digit
  *@b : second digit
  *Return: result
  */
int mul(int a, int b)
{
	return (a *b);
}

/**
  *div - divides a by b
  *@a : first digit
  *@b : second digit
  *Return: result
  */
int div(int a, int b)
{
	return (a / b);
}

/**
  *mod - finds the remainder after a has been divided by b
  *@a : first digit
  *@b : second digit
  *Return: result
  */
int mod(int a, int b)
{
	return (a % b);
}
